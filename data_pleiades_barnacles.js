// simple data integrity test
var errorLog = "";
for (var p in data) {
	if (!data[p].hasOwnProperty("system"))
		errorLog += "Planet ("+p+") has no 'system' property.\n";
	if (!data[p].hasOwnProperty("details"))
		errorLog += "Planet ("+p+") has no 'details' property.\n";
	if (!data[p].hasOwnProperty("poi"))
		errorLog += "Planet ("+p+") has no 'poi' property.\n";
	else {
		for (var poi in data[p]["poi"]) {
			if (!data[p]["poi"][poi].hasOwnProperty("lat"))
				errorLog += "Planet ("+p+") with POI ("+poi+") has no 'lat' property.\n";
			if (!data[p]["poi"][poi].hasOwnProperty("lon"))
				errorLog += "Planet ("+p+") with POI ("+poi+") has no 'lon' property.\n";
			if (!data[p]["poi"][poi].hasOwnProperty("type"))
				errorLog += "Planet ("+p+") with POI ("+poi+") has no 'type' property.\n";
		}
	}
}
